letra = input("Digite uma letra: ")

print("A letra digitada foi: ", letra)

print("Seu valor em decimal é: ", ord(letra))

print("Seu valor em hexadecimal é: ", hex(ord(letra)))

print("Seu valor em binário é: ", bin(ord(letra)))

#input recebe a letra, salva na variável(letra), ord() traduz a letra em decimal,
#  hex() traduz o decimal em hexadecimal, bin() traduz o decimal em binário.
